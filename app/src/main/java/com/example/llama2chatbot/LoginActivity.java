public class LoginActivity extends AppCompatActivity {

    EditText editText;
    Button btnGo;


    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_login);

        editText = findViewById(R.id.etdUsername);
        btnGo = findViewById(R.id.btnGo);

        btnGo.setOnClickListener(v->{

            if(editText.getText().toString().isEmpty()){
                Toast.makeText(this, "please fill name", Toast.LENGTH_SHORT).show();
                return;
            }
            startActivity(new Intent(getApplicationContext(),MainActivity.class).putExtra("name",editText.getText().toString().trim()));
            finish();
        });


    }

}
